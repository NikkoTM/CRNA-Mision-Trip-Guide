let selectedCountry = "";

// -----------------------------
// Event Listener: Dropdown Change
// -----------------------------
countrySelect.addEventListener("change", function () {
  selectedCountry = this.value;
  if (selectedCountry !== "") {
    searchButton.disabled = false;
    searchButton.classList.add("active");
  } else {
    searchButton.disabled = true;
    searchButton.classList.remove("active");
  }
});

// -----------------------------
// Helper Functions
// -----------------------------

// Create a list item with a hyperlink using provided label and URL.
function createLinkedItem(label, url) {
  const li = document.createElement("li");
  const a = document.createElement("a");
  a.href = url;
  a.textContent = label;
  a.target = "_blank";
  li.appendChild(a);
  return li;
}

// Create a grey static list item with the given label.
function createGreyItem(label) {
  const li = document.createElement("li");
  const span = document.createElement("span");
  span.textContent = label;
  span.classList.add("grey-text");
  li.appendChild(span);
  return li;
}

// -----------------------------
// Generate Result Details for Selected Country
// -----------------------------
function generateResult(country) {
  resultList.innerHTML = ""; // Clear previous results
  const data = masterList[country];

  // Array of main fields (in required order)
  const mainFields = [
    { label: "CDC Travel Guide", key: "CDCTravelGuide" },
    { label: "CDC Healthy Travel Packing List", key: "CDCHealthyTravelPackingList" },
    { label: "U.S. State Department Travel Advisory", key: "USStateDeptTravelAdvisory" }
  ];

  // Generate main fields with hyperlinks (if available)
  mainFields.forEach(field => {
    let li;
    if (data[field.key] !== "N/A" && data[field.key]) {
      li = createLinkedItem(field.label, data[field.key]);
    } else {
      li = createGreyItem(field.label);
    }
    resultList.appendChild(li);
  });

  // Add the STEP field right after the US State Department Travel Advisory
  const stepLink = "https://mytravel.state.gov/s/step";
  resultList.appendChild(createLinkedItem("U.S. State Department Smart Traveler Enrollment Program (STEP)", stepLink));

  // Samaritan’s Purse – World Medical Mission field with hospital sub-items if available
  let liSP = document.createElement("li");
  if (data.SamaritanPurse !== "N/A" && data.SamaritanPurse) {
    const a = document.createElement("a");
    a.href = data.SamaritanPurse;
    a.textContent = "Samaritan’s Purse – World Medical Mission";
    a.target = "_blank";
    liSP.appendChild(a);
  } else {
    liSP.appendChild(createGreyItem("Samaritan’s Purse – World Medical Mission").firstChild);
  }
  resultList.appendChild(liSP);

  // Check for Samaritan Hospitals – if present, add as indented sub-items
  if (data.SamaritanHospitals && data.SamaritanHospitals.length > 0) {
    let spSubUl = document.createElement("ul");
    spSubUl.classList.add("sub-item");
    data.SamaritanHospitals.forEach(hospital => {
      let liHospital = createLinkedItem(hospital.label, hospital.url);
      spSubUl.appendChild(liHospital);
    });
    resultList.appendChild(spSubUl);
  }

  // Continue with the remaining fields
  const otherFields = [
    { label: "Mercy Ships (Hospital Ships Charity)", key: "MercyShips" },
    { label: "Health Volunteers Overseas (HVO – Anesthesia Projects)", key: "HVO" },
    { label: "Memorial Hermann – Medical Missions", key: "MemorialHermann" },
    { label: "International Medical Relief (IMR)", key: "IMR" },
    { label: "International Volunteer HQ (IVHQ – Medical Volunteer Programs)", key: "IVHQ" }
  ];

  otherFields.forEach(field => {
    let li;
    if (data[field.key] !== "N/A" && data[field.key]) {
      li = createLinkedItem(field.label, data[field.key]);
    } else {
      li = createGreyItem(field.label);
    }
    resultList.appendChild(li);
  });

  // IFNA Membership – use a custom label when not "N/A"
  let liIFNA;
  if (data.IFNAMembership !== "N/A" && data.IFNAMembership) {
    liIFNA = createLinkedItem("IFNA Membership (Yes: See Standards)", data.IFNAMembership);
  } else {
    liIFNA = createGreyItem("IFNA Membership");
  }
  resultList.appendChild(liIFNA);

  // IFNA Sub-items (Nurse Anesthetist Association and IFNA [Country] Working Abroad)
  let subUl = document.createElement("ul");
  subUl.classList.add("sub-item");
  
  const subLabel1 = "Nurse Anesthetist Association";
  const subLabel2 = "IFNA " + country + " Working Abroad";
  
  let liSub1;
  if (data.IFNASub.NurseAnesthetistAssociation !== "N/A" && data.IFNASub.NurseAnesthetistAssociation) {
    liSub1 = createLinkedItem(subLabel1, data.IFNASub.NurseAnesthetistAssociation);
  } else {
    liSub1 = createGreyItem(subLabel1);
  }
  subUl.appendChild(liSub1);
  
  let liSub2;
  if (data.IFNASub.IFNAWorkingAbroad !== "N/A" && data.IFNASub.IFNAWorkingAbroad) {
    liSub2 = createLinkedItem(subLabel2, data.IFNASub.IFNAWorkingAbroad);
  } else {
    liSub2 = createGreyItem(subLabel2);
  }
  subUl.appendChild(liSub2);
  
  resultList.appendChild(subUl);
}

// -----------------------------
// Event Listener: Search Button Click
// -----------------------------
searchButton.addEventListener("click", function () {
  if (!selectedCountry || !masterList[selectedCountry]) return;
  resultCountryName.textContent = selectedCountry;
  generateResult(selectedCountry);
  // Hide selection page and show result page
  selectionPage.style.display = "none";
  resultPage.style.display = "block";
});

// -----------------------------
// Event Listener: Reset Button Click
// -----------------------------
resetButton.addEventListener("click", function () {
  countrySelect.selectedIndex = 0;
  searchButton.disabled = true;
  searchButton.classList.remove("active");
  selectedCountry = "";
  resultPage.style.display = "none";
  selectionPage.style.display = "block";
});
